function s(title, profileKey, year, opts = {}) {
  const p = P[profileKey];
  if (!p) throw new Error(`Unknown profile: ${profileKey}`);
  const [lo, hi, brightness] = p;
  const obj = { title, artist: opts.artist || profileKey, lo, hi, brightness };
  if (year) obj.year = year;
  if (opts.lo) obj.lo = opts.lo;
  if (opts.hi) obj.hi = opts.hi;
  if (opts.brightness) obj.brightness = opts.brightness;
  if (opts.tags) obj.tags = opts.tags;
  return obj;
}

// Shorthand: [title, artist_display_name, lo, hi, brightness, year, tags?]
